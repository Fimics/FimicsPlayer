package com.mic.demoui.bean;

public class AndroidUI {
    public int type;
    public String name;

    public AndroidUI( String name,int type) {
        this.type = type;
        this.name = name;
    }

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
